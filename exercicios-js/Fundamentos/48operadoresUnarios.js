// exemplo: Operadores unarios.

let num1 = 1
let num2 = 2 

num1++ //operador pois fixada = sua utilizade é acrescentar valor // num1 vale 1(usando ++ passa ser 2)
console.log(num1) ///resultado 2

//ex: pré fixada --

--num1
console.log(num1) //resultado 1
console.log(++num1 === num2--)// da true poq o ++num1 primeiro
console.log(num1 === num2)// vai ser falso poq os valores sao diferentes.